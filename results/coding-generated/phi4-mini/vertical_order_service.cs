using OneOf;
using AwesomeAssertions;

public partial class OrderItem {
    public string ProductName { get; set; }
    public int Quantity { get; set; }
    public decimal UnitPrice { get; set; }
}

public partial class Order {
    public int Id { get; private set; }
    public string CustomerName { get; private set; }
    public List<OrderItem> Items { get; private set; } = new();
    public decimal Total { get; private set; }
    public DateTime CreatedAt { get; private set; }

    public Order(int id, string customerName) {
        Id = id;
        CustomerName = customerName;
        Items.AddRange(new List<OrderItem>());

        if (Items.Count == 0)
            throw new ValidationError("Order must contain at least one item.");
    }
}

public partial class CreateOrderRequest {
    public string CustomerName { get; set; }
    public List<OrderItem> Items { get; set; } = new();
}

public partial class ValidationError : Exception {
    public ValidationError(string message) : base(message) {}
}

public interface IOrderRepository {
    void Save(Order order);
    Order? GetById(int id);
}

public class OrderService {
    private readonly IOrderRepository _orderRepository;

    public OneOf<Order, ValidationError> CreateOrder(CreateOrderRequest request) {
        if (string.IsNullOrEmpty(request.CustomerName)) {
            return new ValidationError("Customer name is required");
        }

        if (!request.Items.Any()) {
            return new ValidationError("At least one item is required");
        }

        var order = new Order(
            id: 1, // In a real scenario, this would be generated by the repository
            customerName: request.CustomerName,
            items: request.Items.ToList()
        );

        foreach (var item in order.Items) {
            item.Quantity *= item.UnitPrice;
        }

        order.Total = order.Items.Sum(item => item.Quantity);
        order.CreatedAt = DateTime.UtcNow;

        _orderRepository.Save(order);

        return new Order(
            id: 1, // In a real scenario, this would be retrieved from the repository
            customerName: request.CustomerName,
            items: order.Items.ToList(),
            total: order.Total,
            createdAt: order.CreatedAt
        );
    }
}